                    <h3 id='6防开盒'><span>6.防开盒</span></h3>
                    <h5 id='61开盒原理'><span>6.1开盒原理</span></h5>
                    <p><span>开盒，指打开潘多拉的盒子，通过网络技术手段查找蛛丝马迹最后挖出身份信息，使用网络平台泄露的各种信息组成的具有高强度粘性信息集合，也就是社工库，通过一个或多个关键点信息就可以查询出所有相关信息，其中包括身份证，手机号，社交平台账号等。</span>
                    </p>
                    <h5 id='62防开盒手段'><span>6.2防开盒手段</span></h5>
                    <p><span>在进行虚拟形象活动时，为了避免个人信息泄露导致的风险，需要做到以下几点：</span></p>
                    <ol start=''>
                        <li><span>将三次元和二次元账号隔离开来，包括禁止使用当前正在使用的QQ号和微信号，禁止使用支付宝转账，禁止使用相同的网络名称，或者关注了自己的小号等等。</span></li>
                        <li><span>如果不可避免地需要使用这些信息，选择一个信赖的朋友来帮助收款付款和进行网络活动。</span></li>
                        <li><span>虚拟主播账号必须完全隔离于个人注册的其他账号，以有效避免被开盒的风险</span></li>
                        <li><span>注意如果电话或者网络上需要填写个人信息的时候先确定是否是官方网站防止被钓鱼</span></li>
                        <li><span>如果自己没有订外卖但受到了外卖后一定要拒收</span></li>
                    </ol>
                    <h5 id='63被开盒后该怎么办'><span>6.3被开盒后该怎么办?</span></h5>
                    <p><span>保存截图证据，并且报警起诉，根据《最高人民法院、最高人民检察院关于办理侵犯公民个人信息刑事案件适用法律若干问题的解释》，非法获取、出售或者提供行踪轨迹信息、通信内容、征信信息、财产信息等个人敏感信息五十条以上的，即构成犯罪，处三年以下有期徒刑或者拘役。
                            以保证自己的安全。</span></p>
